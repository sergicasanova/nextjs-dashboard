// Este archivo contiene definiciones de tipos para tus datos.
// Describe la forma de los datos, y qué tipo de datos debe aceptar cada propiedad.
// Para simplificar la enseñanza, estamos definiendo manualmente estos tipos.
// Sin embargo, estos tipos se generan automáticamente si estás usando un ORM como Pris
export type User = {
  id: string;
  name: string;
  email: string;
  password: string;
};

export type Customer = {
  id: string;
  name: string;
  email: string;
  image_url: string;
};

export type Invoice = {
  id: string;
  customer_id: string;
  amount: number;
  date: string;
  // En TypeScript, esto se llama un tipo de unión de cadena.
  // Significa que la propiedad "status" solo puede ser una de las dos cadenas: 'pending' o 'paid'.
  status: 'pending' | 'paid';
};

export type Revenue = {
  month: string;
  revenue: number;
};

export type LatestInvoice = {
  id: string;
  name: string;
  image_url: string;
  email: string;
  amount: string;
  status: 'pending' | 'paid';
};

// La base de datos devuelve un número para el importe, pero después lo formateamos a una cadena con la función formatCurrency
export type LatestInvoiceRaw = Omit<LatestInvoice, 'amount'> & {
  amount: number;
};

